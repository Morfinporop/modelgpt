export function generateCharacter(): Character {
  const randomItem = <T,>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)];

  return {
    gender: randomItem(genders),
    age: Math.floor(Math.random() * 50) + 18, // 18-67
    profession: randomItem(professions),
    health: randomItem(healthStates),
    bodyType: randomItem(bodyTypes),
    hobby: randomItem(hobbies),
    phobia: randomItem(phobias),
    baggage: randomItem(baggage),
    fact1: randomItem(facts),
    fact2: randomItem(facts.filter(f => f !== randomItem(facts))),
    specialAbility: randomItem(specialAbilities),
  };
}

export function generateApocalypse() {
  const randomItem = <T,>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)];
  
  return {
    scenario: randomItem(apocalypseScenarios),
    condition: randomItem(bunkerConditions),
  };
}
